public class For {
    public static void main(String[] args) {
        System.out.println("-----------------");
        System.out.println("-----------------");
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                System.out.println(" i = " + i + ", j = " + j);
            }
        }
    }
}
